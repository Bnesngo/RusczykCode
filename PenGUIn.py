from tkinter import *
log = []

class root: #this is basic. Destroy and create.
  def __init__():
    pass
  def destroy():
    self = None
  def create():
    log.append(self)

class Misc (root):
  pass

class Guin (Misc):
  def __init__():
    self.guin = Tk()
    return self.guin
  
  def __str__():
    return "Guin window"
  
  def title(new_title):
    self.guin.title(new_title)

class Tux (Guin):
  def __init__(height, width, master):
    self.tux = Canvas(height=height, width=width, master=master)
    self.master = master
    return self.tux
  
  def getNewTux(height, width, master):
    return Canvas(height=height, width=width, master=master)
  
  
    
    
    
  
  
